/** 从附注字符串解析出勾选的词条 + 射程值（XX/XX）+ 弹药类别 */
export function parseWeaponNoteToTraits(note) {
  const traits = []
  let range = ''
  let ammoCategory = ''
  if (!note || typeof note !== 'string') return { traits, range, ammoCategory }
  const s = note.trim()
  if (!s || s === '—') return { traits, range, ammoCategory }
  const rangeMatch = s.match(/(\d+)\s*\/\s*(\d+)/)
  if (rangeMatch) range = `${rangeMatch[1]}/${rangeMatch[2]}`
  const ammoBlockMatch = s.match(/弹药\s*[（(]\s*(?:射程\s*)?(\d+\s*\/\s*\d+)?\s*[；;]\s*([^）)]+)[）)]/)
  if (ammoBlockMatch) {
    ammoCategory = (ammoBlockMatch[2] || '').trim()
  } else {
    const ammoParenMatch = s.match(/弹药\s*[（(]\s*([^）)]+)[）)]/)
    if (ammoParenMatch) {
      const inner = (ammoParenMatch[1] || '').trim()
      if (/[；;]/.test(inner)) {
        const afterSemi = inner.split(/[；;]/).pop()?.trim()
        if (afterSemi && !/^\d+\s*\/\s*\d+$/.test(afterSemi)) ammoCategory = afterSemi
      } else if (inner && !/^\s*射程\s*\d+\s*\/\s*\d+\s*$/i.test(inner)) {
        ammoCategory = inner
      }
    }
  }
  WEAPON_TRAIT_OPTIONS.forEach((t) => {
    if (t === '射程') {
      if (/\d+\s*\/\s*\d+/.test(s) || /射程/i.test(s)) traits.push(t)
    } else if (s.includes(t)) traits.push(t)
  })
  return { traits, range, ammoCategory }
}

/** 从词条勾选 + 射程值 + 弹药类别构建附注字符串 */
export function buildWeaponNoteFromTraits(traits, range, ammoCategory) {
  if (!Array.isArray(traits) || traits.length === 0) return ''
  const rest = traits.filter((t) => t !== '射程' && t !== '弹药')
  const parts = []
  if (traits.includes('弹药')) {
    const rangePart = range && /^\d+\/\d+$/.test(String(range).trim()) ? `射程 ${range.trim()}` : ''
    const ammoPart = [rangePart, (ammoCategory || '').trim()].filter(Boolean).join('；')
    parts.push(ammoPart ? `弹药（${ammoPart}）` : '弹药')
  }
  if (traits.includes('射程') && !traits.includes('弹药')) parts.push(range && /^\d+\/\d+$/.test(String(range).trim()) ? `射程（${range.trim()}）` : '射程')
  return [...parts, ...rest].join('，')
}

