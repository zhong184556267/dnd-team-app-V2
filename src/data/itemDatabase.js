/** 参与 Buff「武器类别」筛选的武器顶层类型 */
export const WEAPON_TYPES_FOR_BUFF_SCOPE = ['近战武器', '远程武器', '枪械']

/**
 * 命中/伤害加值「武器类别」多选选项：三大类型 + 库内上述类型全部「类别」（如长剑、手铳）。
 * 匹配时同时比对物品 proto.类型 与 proto.类别（见 buffTypes.weaponProtoMatchesBuffWeaponCategories）。
 */
export const WEAPON_BUFF_CATEGORY_SELECT_OPTIONS = (() => {
  const typeOpts = WEAPON_TYPES_FOR_BUFF_SCOPE.map((t) => ({ value: t, label: t }))
  const seen = new Set(WEAPON_TYPES_FOR_BUFF_SCOPE)
  const cats = []
  for (const item of ITEM_DATABASE) {
    if (!WEAPON_TYPES_FOR_BUFF_SCOPE.includes(item.类型)) continue
    const c = String(item.类别 ?? '').trim()
    if (!c || seen.has(c)) continue
    seen.add(c)
    cats.push(c)
  }
  cats.sort((a, b) => a.localeCompare(b, 'zh-Hans-CN'))
  return [...typeOpts, ...cats.map((c) => ({ value: c, label: c }))]
})()

/** 显示名：有自定义名称用名称，否则用类别 */
export function getItemDisplayName(item) {
  if (!item) return '—'
  return (item.名称 && String(item.名称).trim()) ? String(item.名称).trim() : (item.类别 || '—')
}

/** 根据 id 查找（先查内置，再查自定义） */
export function getItemById(id) {
  const built = ITEM_DATABASE.find((x) => x.id === id)
  if (built) return { ...built }
  const custom = getCustomItems().find((x) => x.id === id)
  return custom ? { ...custom } : null
}

/** 自定义物品列表 */
export function getCustomItems() {
  if (isSupabaseEnabled()) {
    return Array.isArray(customItemsRemoteCache) ? [...customItemsRemoteCache] : []
  }
  try {
    const raw = localStorage.getItem(CUSTOM_ITEMS_KEY)
    const list = raw ? JSON.parse(raw) : []
    return Array.isArray(list) ? list : []
  } catch {
    return []
  }
}

function saveCustomItemsLocal(list) {
  try {
    localStorage.setItem(CUSTOM_ITEMS_KEY, JSON.stringify(list))
  } catch (_) {}
}

function persistCustomItems(list) {
  if (isSupabaseEnabled()) {
    customItemsRemoteCache = [...list]
    return teamData.saveCustomLibrary('custom_items', customItemsRemoteCache)
  }
  saveCustomItemsLocal(list)
  return Promise.resolve()
}

/** 已删除类型：首饰→饰品，瓶罐→药品，武器→近战武器，爆炸物→消耗品，奇物/液体/套组→其他 */
function normalizeItemType(类型) {
  if (类型 === '首饰') return '饰品'
  if (类型 === '瓶罐') return '药品'
  if (类型 === '武器') return '近战武器'
  if (类型 === '爆炸物') return '消耗品'
  if (['奇物', '液体', '套组'].includes(类型)) return '其他'
  return ITEM_TYPES.includes(类型) ? 类型 : '其他'
}

/** 供下拉使用的完整物品列表（内置 + 自定义），显示名用于选项文案 */
export function getItemList() {
  const custom = getCustomItems()
  return [
    ...ITEM_DATABASE.map((x) => ({ ...x, _display: getItemDisplayName(x) })),
    ...custom.map((x) => {
      const 类型 = normalizeItemType(x.类型)
      return { ...x, 类型, _display: getItemDisplayName({ ...x, 类型 }) }
    }),
  ]
}

/**
 * 按 类型 → 子类型 分组的物品列表，供多层级选择器使用。
 * 返回：{ type: string, subTypes: { subType: string, items: item[] }[] }[]
 * 无子类型的归入 subType ''（显示为「全部」）
 */
export function getItemListGrouped() {
  const all = getItemList()
  const byType = {}
  const typesToShow = ITEM_TYPES.concat('其他')
  typesToShow.forEach((t) => { byType[t] = {} })
  all.forEach((x) => {
    const t = x.类型 && ITEM_TYPES.includes(x.类型) ? x.类型 : '其他'
    const st = x.子类型 && String(x.子类型).trim() ? String(x.子类型).trim() : ''
    if (!byType[t]) byType[t] = {}
    if (!byType[t][st]) byType[t][st] = []
    byType[t][st].push(x)
  })
  return typesToShow.map((type) => {
    const subMap = byType[type] || {}
    const subTypes = Object.keys(subMap).sort((a, b) => (a === '' ? 1 : b === '' ? -1 : a === '弹药' ? 1 : b === '弹药' ? -1 : a.localeCompare(b)))
    return {
      type,
      subTypes: subTypes.length ? subTypes.map((subType) => ({ subType: subType || '全部', items: subMap[subType] })) : [{ subType: '全部', items: [] }],
    }
  })
}

/** 新增自定义物品；返回新项（含 id）。类型会规范化（首饰→饰品，奇物/液体/套组→其他）。 */
export function addCustomItem(item) {
  const list = getCustomItems()
  const id = 'custom_' + Date.now()
  const 类型 = normalizeItemType(item.类型) || '近战武器'
  const newItem = {
    id,
    类型,
    子类型: item.子类型?.trim() || '',
    类别: item.类别?.trim() || '自定义',
    名称: item.名称?.trim() || '',
    攻击: item.攻击?.trim() || '',
    附注: item.附注?.trim() || '',
    精通: item.精通?.trim() || '',
    伤害: item.伤害?.trim() || '',
    重量: item.重量?.trim() || '',
    价格: item.价格?.trim() || '',
    详细介绍: item.详细介绍?.trim() || '',
  }
  list.push(newItem)
  const p = persistCustomItems(list)
  if (p && typeof p.then === 'function') return p.then(() => newItem)
  return newItem
}

/** 更新自定义物品；若 patch 含 类型，会规范化（首饰→饰品，奇物/液体/套组→其他） */
export function updateCustomItem(id, patch) {
  const list = getCustomItems()
  const idx = list.findIndex((x) => x.id === id)
  if (idx === -1) return null
  const p = patch.类型 !== undefined ? { ...patch, 类型: normalizeItemType(patch.类型) } : patch
  list[idx] = { ...list[idx], ...p }
  const pr = persistCustomItems(list)
  if (pr && typeof pr.then === 'function') return pr.then(() => list[idx])
  return list[idx]
}

/** 删除自定义物品 */
export function removeCustomItem(id) {
  const list = getCustomItems().filter((x) => x.id !== id)
  const pr = persistCustomItems(list)
  if (pr && typeof pr.then === 'function') return pr.then(() => true)
  return Promise.resolve(true)
}
